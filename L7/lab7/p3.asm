DATA SEGMENT
    BLOCK1 DB 'MALAYALAM'
    MSG1 DB "It is palindrome $"
    MSG2 DB "It is not palindrome $"
DATA ENDS
 
PRINT MACRO MSG
    MOV AH,09H
    LEA DX,MSG
    INT 21H
    INT 3H
ENDM
 
EXTRA SEGMENT
    BLOCK2 DB 9 DUP(?)
EXTRA ENDS
 
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:EXTRA
START: 
    MOV AX,DATA
    MOV DS,AX
    MOV AX,EXTRA
    MOV ES,AX
    LEA SI,BLOCK1
    LEA DI,BLOCK2+8
    MOV CX,00009H
BACK: 
    CLD
    LODSB
    STD
    STOSB
    LOOP BACK

    LEA SI,BLOCK1
    LEA DI,BLOCK2
    MOV CX,0009H
    CLD
    REPZ CMPSB
    JNZ SKIP
    PRINT MSG1
SKIP: 
    PRINT MSG2
CODE ENDS
END START